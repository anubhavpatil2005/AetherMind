import { Router } from "express";

import { authenticate } from "../middleware/auth";

import {

    createNode,

    createChildNode,

    duplicateNode,

    getNodes,

    updateNode,

    deleteNode

} from "../controllers/nodeController";

const router = Router();

/*
|--------------------------------------------------------------------------
| Protected Routes
|--------------------------------------------------------------------------
*/

router.use(authenticate);

/*
|--------------------------------------------------------------------------
| Create Child Node
|--------------------------------------------------------------------------
*/

router.post(

    "/child",

    createChildNode

);

/*
|--------------------------------------------------------------------------
| Create Node
|--------------------------------------------------------------------------
*/

router.post(

    "/",

    createNode

);

/*
|--------------------------------------------------------------------------
| Get Nodes of MindMap
|--------------------------------------------------------------------------
*/

router.get(

    "/:mindmapId",

    getNodes

);

/*
|--------------------------------------------------------------------------
| Update Node
|--------------------------------------------------------------------------
*/

router.put(

    "/:id",

    updateNode

);

/*
|--------------------------------------------------------------------------
| Delete Node
|--------------------------------------------------------------------------
*/

router.delete(

    "/:id",

    deleteNode

);

/*
|--------------------------------------------------------------------------
| Duplicate Node
|--------------------------------------------------------------------------
*/

router.post(

    "/:id/duplicate",

    duplicateNode

);

export default router;